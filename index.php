                <h1 id="XPScanner">XPScanner: User Manual</h1>
                <p>
                    XPScanner is an easy and simple product tracking and management application with a goal to help every user to track, 
                    manage, analyze and stay compliant with their product expiration dates. 
                </p>
                        
                <p>
                    XPScanner is a single-user kind of system that allows users to register products that accurately manage and track the expiration date based accordingly 
                    to the product’s description that is entered by the user and runs over a platform like Android Smartphone and soon to IOS operating system devices. 
                    In addition, the system also helps control food waste such as expiry labels confusion, premature binning, overbuying, and etc.;
                    driving efficiency ensuring the best user experience possible, and turning prior productivity loss.
                </p>

                <h1 id="XPScanner1">Getting Started</h1>

                <h2 id="XPScanner2">Login</h2>
                <p>
                    To login, you can sign up using any preferred email, as long as it is currently working, 
                    then you can login using the new account made upon finishing the two-form authentication. 
                </p> 

                <p>
                    To signup, go to Sign In then press the Register right below the Login button, and fill the signup form to fully create an account.
                </p>

                <p>Note: Two-Factor Authentication must be performed before having an access to the system in order to avoid account duplication and poor account security.</p>

                <h2 id="XPScanner3">Settings</h2>
                <p>
                    Below the dashboard inside the navigation bar; About, Scan Products, Manage Products and Expired Products settings can be viewed.
                </p>


                <h1 id="XPScanner4">Adding Products</h1>
                <p>
                    When you add a product, you can either scan the product barcode or manually enter the barcode number. 
                    Upon product registration, you can enter the product’s category, description and expiration directly before saving it. 
                    If multiple products are going to be registered, you can keep tack on it through mentioning it on its product's description information.
                    Go to Scan Products setting, then either choose Scan Barcode or Enter Barcode product
                </p>

                <h2  id="XPScanner5">Scan a Barcode</h2>
                <p>
                    •	Upon selecting Scan Barcode, closely point a barcode to camera, if the camera detects the barcode, the system automatically registers the product's barcode number.
                    <br>
                    •	If the camera won't clearly recognize the barcode number, you can enter the barcode number by yourself, select Enter Barcode and type the numbers.
                    <br>
                    •	Then select preferred category. To login, you can sign up using any preferred email, as long as it is currently working, then you can login using the new account made upon finishing the two-form authentication. 

                </p>

                <h2 id="XPScanner6">Enter Expiration Date</h2>
                <p> 
                    • 	Enter the expiration date of your product.
                    <br>
                    •	Make a note. This note will be displayed right after the expiration date.
                    <br>
                    •	If you leave any blank spaces, product registration won't be saved.
                </p>

                <h2 id="XPScanner7"> Go to Manage Products setting, to view, edit, or remove registered products.</h2>
                <p>
                    You can edit the overall information of the product of any listed products in case there are some mistakes happened.
                    Just press the green icon to view the product, press the blue icon to edit the product and press the red icon to delete the product.
                    To signup, go to Sign In then press the Register right below the Login button, and fill the signup form to fully create an account. 
                    You can also remove any listed products in case of scanned product duplications. 
                    Just press the red icon to remove product.
                    <br>
                    Search or sort products by categories can be done through typing the corresponding keywords of the products on the search bar.
                    Just press the search bar or and type the keywords you wish to use. You can also use the scan product to scan the product’s barcode.
                    Note: Words made in the product description can also be used.
                </p>

                <h2 id="XPScanner8">Expired Products Deletion</h2>
                <p>
                    Go to Expired Products setting, to view and delete expired registered products.
                    Expired registered products can be easily viewed on the Expired Products section, so deletion will be convenient for you without affecting some unexpired product when removing. 
                    Just press the red icon to delete the specific expired registered product and you can also delete all the expired products just by pressing the delete all button.
                </p>
                
